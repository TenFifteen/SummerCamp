/**
 * Problem: Write a function that add two numbers A and B.
 *          You should not use + or any arithmetic operators.
 * Solve: just add
 * Tips: no
 */
class Solution {
    public:
        /*
         * @param a: The first integer
         * @param b: The second integer
         * @return: The sum of a and b
         */
        int aplusb(int a, int b) {
            return a + b;
        }
};
